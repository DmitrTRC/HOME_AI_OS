-- ============================================================
-- Этот скрипт выполняется ОДИН раз при первом создании БД.
-- Он подключает расширения PostgreSQL которые нам нужны.
-- ============================================================

-- pgvector: добавляет тип vector() для хранения embeddings
-- Без этого расширения семантический поиск не работает
CREATE EXTENSION IF NOT EXISTS vector;

-- pg_trgm: триграммный индекс для нечёткого поиска (fuzzy search)
-- Позволяет искать "Москва" даже если написали "Масква"
CREATE EXTENSION IF NOT EXISTS pg_trgm;

-- uuid-ossp: генерация UUID (уникальных идентификаторов)
-- gen_random_uuid() есть в PG 13+, но для надёжности ставим
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
